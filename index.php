					<blockquote></blockquote>
					<h2 class="mt-4">O Conceito de Programação</h2>
					<p>De forma geral, a programação é um processo de escrita, testes e manutenção de programas de
						computadores. Esses programas, por sua vez, são compostos por conjuntos de instruções
						determinados
						pelo programador que descrevem tarefas a serem realizadas pela máquina e atendem diversas
						finalidades.
						O advento da programação está diretamente ligado à história dos computadores. A primeira
						programadora foi <a href="https://idocode.com.br/blog/tecnologia/mulheres-importantes-na-tecnologia/">uma
							mulher
							chamada Ada Lovelace</a>, que escreveu um código para possibilitar a utilização da <a href="https://www.tecmundo.com.br/historia/16641-charles-babbage-um-cientista-muito-alem-de-seu-tempo.htm">máquina
							analítica de Charles Babbage</a>, uma máquina robusta, de difícil comunicação,
						considerada a
						precursora dos computadores eletrônicos atuais. Mas, para compreender de verdade do que ela
						se
						trata, é importante conhecer dois elementos fundamentais que compõem todos os computadores
						modernos,
						que são o hardware e o software.</p>
					<h3 class="mt-4">Hardwares, softwares e programação</h3>
					<p>Um computador é formado por componentes físicos, como a parte externa do seu celular, a CPU
						de um PC,
						a memória, o mouse, enfim… os meios pelos quais os sinais elétricos podem ser convertidos em
						dados,
						informações.
						Esses componentes são chamados de hardware e a sua linguagem binária, que é a linguagem de
						máquina,
						é composta apenas por bits, que são zeros e uns. Os bits representam a ausência ou presença
						de
						sinais elétricos. Agora, imagina utilizar o seu celular tendo que interpretar informações
						binárias:
						se tudo o que você visse na tela fosse uma sequência infinita de zeros e uns, os celulares
						não
						seriam nada práticos, não é mesmo? É por causa disso que existem os softwares. </p>
					<blockquote></blockquote>
					<p>O software é o meio pelo qual a linguagem de máquina pode ser compilada ou interpretada,
						através de
						códigos criados em uma linguagem intermediária, para idiomas que conhecemos, como o
						português, o
						inglês e também para imagens, cores, números… enfim, uma série de dados que podem ser
						assimilados
						mais facilmente pelos seres humanos.
					<blockquote></blockquote>
					Alguns exemplos de softwares são os programas de computadores, como o Word, Excel e Paint, e as
					aplicações para celulares, como a Playstore, App Store ou o Uber. Os sistemas operacionais, como
					o
					Android, Windows, Linux ou o Mac, também podem ser vistos, de forma simplificada, como softwares
					gigantes que comportam outros softwares.
					<blockquote></blockquote>
					Mas, afinal, qual é a relação entre hardware, software e programação? A programação é exatamente
					quem possibilita a existência dos softwares e, por consequência, a utilização mais prática dos
					hardwares. Para poder dar origem aos softwares, a programação ganha uma linguagem própria que
					compõe
					códigos escritos por programadores.
					</p>
					<h3 class="mt-4">Linguagem de Programação</h3>
					<p>Essa linguagem nada mais é do que um método padronizado que permite comunicar instruções
						específicas
						para um computador. Há centenas de variações e todas elas permitem que o programador defina
						sobre
						quais dados o computador deverá atuar, como armazenar e transmitir esses dados e quais ações
						tomar
						sob variadas circunstâncias.

						Há dois tipos de linguagem para o programador: as compiladas e as interpretadas. Você
						compreenderá
						melhor cada uma delas no tópico “Código fonte” mais abaixo.
					</p>
					<h3 class="mt-4">Código Fonte</h3>
					<p>Código fonte é o conjunto de palavras e comandos escritos ordenadamente, de maneira lógica,
						que
						contém instruções em determinada linguagem de programação.
						Como dissemos, há dois tipos de linguagem na hora de programar: as compiladas e as
						interpretadas.
						<strong>Compilar</strong> uma linguagem de programação nada mais é do que transformar o
						código fonte
						em algo que faça sentido para a máquina, ou seja, transformar o que o programador escreveu
						em
						<strong>linguagem de máquina.</strong> Já no processo de <strong>interpretação</strong>, o
						código
						fonte é transformado em uma <strong>linguagem intermediária </strong>que, por sua vez, será
						interpretada pela máquina virtual durante a execução do programa.
					</p>
					<h3 class="mt-4">Habilidades de um programador</h3>
					<p>Agora que você já entendeu o que é a programação, como se define a sua linguagem e o
						significado de
						código fonte, é importante também saber que muito além de um simples processo de escrita, a
						programação envolve um misto de outras habilidades cognitivas, como o raciocínio lógico,
						habilidades
						matemáticas, a capacidade de abstração e de lidar com diferentes tipos de dados.

						Quando começa-se a estudar programação, a primeira coisa que se aprende é a criar algoritmos
						básicos, que são como receitas, para que a partir de um passo a passo, uma tarefa possa ser
						executada. E esse algoritmo é desenvolvido a partir de tentativas e erros baseados no
						resultado em
						que se quer chegar e em como se pode alcançá-lo.
						Por esse motivo é importante dedicar tempo ao aprendizado da programação, e quanto mais cedo
						uma
						pessoa começa a se dedicar a programação, mais cedo também ela alcança resultados mais
						complexos, se
						tornando um programador cognitivamente capacitado para criar estratégias e soluções
						tecnológicas
						muito úteis a nossa sociedade, como softwares médicos, de engenharia, entre outros.
					</p>
